export async function buildJoinderDocx(input: SubscriptionDocInput): Promise<Buffer> {
  const d = input.data;
  const signed = input.signName && input.signedAt;
  const sigLine = signed
    ? `/s/ ${input.signName} — signed electronically on ${input.signedAt!.toISOString().slice(0, 10)}`
    : "_________________________________    Date: _______________";

  const doc = pageShell(`Amendment & Joinder — ${input.poolName}`, [
    CENTER("AMENDMENT AND JOINDER", { bold: true, size: 30 }),
    CENTER("to the Operating Agreement — Admission of Additional Member", { bold: true }),
    CENTER(""),
    P(null, [
      t(`AMENDMENT No. ___ TO THE OPERATING AGREEMENT OF ${input.poolName.toUpperCase()}. `, { bold: true }),
      t(
        "Pursuant to Sections 3.3, 3.4 and 11.2(a) of the Operating Agreement, the Manager hereby (a) admits the undersigned subscriber (the “New Member”) as a Member of the Company, effective upon the Manager's acceptance below; and (b) amends and restates Exhibit A in the form attached hereto to reflect such admission and the corresponding Units and Capital Contribution.",
      ),
    ]),
    P(
      "Joinder of New Member.",
      "The undersigned New Member, in consideration of its admission to the Company, hereby (i) joins and agrees to be bound by all terms and conditions of the Operating Agreement as a Member; (ii) confirms its subscription agreement and the Capital Contribution set forth on the restated Exhibit A; (iii) delivers the applicable IRS Form W-9 or W-8 series form; and (iv) acknowledges the tax provisions of Article VI of the Operating Agreement.",
    ),
    P(null, [t("NEW MEMBER:", { bold: true })]),
    P(null, blank(d.legalName)),
    P(null, `By: ${sigLine}`),
    ...(signed
      ? [
          P(null, [
            t(
              `Electronic signature record: name typed "${input.signName}" · IP ${blank(input.signIp, "n/a")} · SHA-256 ${blank(
                input.signHash,
                "n/a",
              )}.`,
              { size: 16, color: "666666" },
            ),
          ]),
        ]
      : []),
    P(null, [t("ACCEPTED — MANAGER (sole signature required to effect the admission and restate Exhibit A):", { bold: true })]),
    P(null, input.managerName),
    P(null, "By: _________________________________    Date: _______________"),
    P(null, ""),
    P(null, [
      t("Attachment: ", { bold: true }),
      t("Exhibit A, as amended and restated as of the date of this Amendment (generated from the Company's capital ledger)."),
    ]),
  ]);

  return Packer.toBuffer(doc) as Promise<Buffer>;
}
