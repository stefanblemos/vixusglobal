import {
  AlignmentType,
  Document,
  Footer,
  Header,
  HeadingLevel,
  Packer,
  PageNumber,
  Paragraph,
  TextRun,
} from "docx";
import { ACCREDITATION_OPTIONS, type WizardData } from "@/lib/subscription/types";

const FONT = "Times New Roman";
const SZ = 22;
const t = (text: string, opts: Record<string, unknown> = {}) =>
  new TextRun({ text, font: FONT, size: SZ, ...opts });

const H1 = (n: string, text: string) =>
  new Paragraph({
    heading: HeadingLevel.HEADING_1,
    spacing: { before: 320, after: 160 },
    children: [t(`PART ${n} — ${text}`, { bold: true, size: 24 })],
  });

const P = (label: string | null, text: string | TextRun[], opts: Record<string, unknown> = {}) =>
  new Paragraph({
    spacing: { after: 120 },
    alignment: AlignmentType.JUSTIFIED,
    children: [...(label ? [t(`${label} `, { bold: true })] : []), ...(Array.isArray(text) ? text : [t(text)])],
    ...opts,
  });

const LI = (text: string) =>
  new Paragraph({
    spacing: { after: 60 },
    alignment: AlignmentType.JUSTIFIED,
    indent: { left: 720 },
    children: [t(text)],
  });

const CENTER = (text: string, opts: Record<string, unknown> = {}) =>
  new Paragraph({ alignment: AlignmentType.CENTER, spacing: { after: 120 }, children: [t(text, opts)] });

export type SubscriptionDocInput = {
  poolName: string; // razão social da LLC (ex.: "Vixus Home Partners I LLC")
  poolCode: string;
  managerName: string; // entidade gestora
  county: string;
  unitPrice: number;
  data: WizardData;
  signName?: string | null;
  signedAt?: Date | null;
  signIp?: string | null;
  signHash?: string | null;
};

const money = (n: number) =>
  "US$" + n.toLocaleString("en-US", { minimumFractionDigits: 2, maximumFractionDigits: 2 });

const blank = (s: string | undefined | null, fallback = "________________") =>
  s && s.trim() ? s.trim() : fallback;

function pageShell(footerLeft: string, children: Paragraph[]) {
  return new Document({
    styles: { default: { document: { run: { font: FONT, size: SZ } } } },
    sections: [
      {
        properties: {
          page: {
            size: { width: 12240, height: 15840 },
            margin: { top: 1440, bottom: 1440, left: 1440, right: 1440 },
          },
        },
        headers: {
          default: new Header({
            children: [
              new Paragraph({
                alignment: AlignmentType.CENTER,
                children: [
                  t("GENERATED BY THE VIXUS PLATFORM — SUBJECT TO MANAGER ACCEPTANCE", {
                    bold: true,
                    color: "666666",
                    size: 16,
                  }),
                ],
              }),
            ],
          }),
        },
        footers: {
          default: new Footer({
            children: [
              new Paragraph({
                alignment: AlignmentType.CENTER,
                children: [
                  t(`${footerLeft}  ·  Page `, { size: 16, color: "666666" }),
                  new TextRun({ children: [PageNumber.CURRENT], font: FONT, size: 16, color: "666666" }),
                ],
              }),
            ],
          }),
        },
        children,
      },
    ],
  });
}

function signatureBlock(input: SubscriptionDocInput): Paragraph[] {
  const d = input.data;
  const isIndividual = d.type === "INDIVIDUAL";
  const signed = input.signName && input.signedAt;
  const sigLine = signed
    ? `/s/ ${input.signName} — signed electronically on ${input.signedAt!.toISOString().slice(0, 10)}`
    : "_________________________________    Date: _______________";
  const audit = signed
    ? [
        P(null, [
          t(
            `Electronic signature record: name typed "${input.signName}" · IP ${blank(input.signIp, "n/a")} · SHA-256 ${blank(
              input.signHash,
              "n/a",
            )}. The Investor consented to electronic execution (E-SIGN/UETA).`,
            { size: 16, color: "666666" },
          ),
        ]),
      ]
    : [];
  return [
    new Paragraph({
      spacing: { before: 400, after: 200 },
      children: [t("IN WITNESS WHEREOF, the Investor has executed this Subscription Agreement as of the date stated below.")],
    }),
    P(null, [t(isIndividual ? "INVESTOR (individual):" : "INVESTOR (entity or trust):", { bold: true })]),
    ...(isIndividual ? [] : [P(null, blank(d.legalName))]),
    P(null, `By: ${sigLine}`),
    P(null, `Name: ${blank(input.signName ?? d.legalName)}${isIndividual ? "" : " · Title: authorized signatory"}`),
    ...audit,
    P(null, [
      t("The Investor is simultaneously executing the Amendment and Joinder in the form of Exhibit B to the Operating Agreement.", {
        italics: true,
        size: 20,
      }),
    ]),
    new Paragraph({ spacing: { before: 300, after: 120 }, children: [t("ACCEPTANCE", { bold: true })] }),
    P(null, [
      t(
        `Accepted as of _______________, for ${d.units ?? "____"} Units and a Capital Commitment of ${
          d.units ? money(d.units * input.unitPrice) : "____"
        }.`,
      ),
    ]),
    P(null, `${input.poolName}`),
    P(null, `By: ${input.managerName}, its Manager`),
    P(null, "By: _________________________________    Date: _______________"),
  ];
}

